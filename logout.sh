#!/bin/sh
#-----------------------------------------
#功能：注销操作，会造成所有用这个账号登录的IP下线
#返回：0
#-----------------------------------------

run () {
  echo "进行注销操作"
  echo "------------------"
  timeout -s 2 10 phantomjs --ignore-ssl-errors=true $DIR/logout.js
  echo "------------------"
  echo "注销操作完毕"
}

run
